class TestSlurmMultiPrepareScript:
    """`_prepare_slurm_multi_script` emits every model_info.env_vars as `export`."""

    @pytest.fixture
    def slurm_deployment(self, tmp_path: Path) -> SlurmDeployment:
        """Build a SlurmDeployment whose manifest contains the MAD-private #186 entry."""
        # _prepare_slurm_multi_script resolves model_info["scripts"] against the
        # manifest's directory and bails (returns False) if the script doesn't exist.
        # Create a placeholder so the existence check passes; contents are irrelevant
        # because prepare() never executes it.
        script_rel = PR186_MODEL_ENTRY["scripts"]
        script_abs = tmp_path / script_rel
        script_abs.parent.mkdir(parents=True, exist_ok=True)
        script_abs.write_text("#!/bin/bash\n# placeholder slurm_multi model script for unit test\n")

        # Use the model image-name as the manifest key (matches manifest convention).
        image_key = "rocm/pytorch-private:sglang_disagg_mori_20260502"

        manifest = {
            "built_images": {
                image_key: {
                    "image_name": image_key,
                    "docker_image": image_key,
                    "registry_image": image_key,
                },
            },
            "built_models": {
                image_key: PR186_MODEL_ENTRY,
            },
            "context": {
                "docker_env_vars": {},
                "docker_mounts": {},
                "docker_build_arg": {},
                "gpu_vendor": "AMD",
                "guest_os": "UBUNTU",
                "docker_gpus": "all",
            },
        }
        manifest_path = tmp_path / "build_manifest.json"
        manifest_path.write_text(json.dumps(manifest))

        # Additional context drives the slurm config: point output_dir at tmp_path
        # so we can read the generated script back.
        additional_context = {
            "deploy": "slurm",
            "gpu_vendor": "AMD",
            "guest_os": "UBUNTU",
            "slurm": dict(PR186_MODEL_ENTRY["slurm"], output_dir=str(tmp_path / "slurm_results")),
            "distributed": PR186_MODEL_ENTRY["distributed"],
        }

        cfg = DeploymentConfig(
            target="slurm",
            manifest_file=str(manifest_path),
            additional_context=additional_context,
        )
        return SlurmDeployment(cfg)

    def test_prepare_dispatches_to_slurm_multi(self, slurm_deployment, tmp_path):
        """prepare() must take the slurm_multi early-dispatch path and return True."""
        ok = slurm_deployment.prepare()
        assert ok is True
        # Wrapper script should now exist
        assert slurm_deployment.script_path is not None
        assert Path(slurm_deployment.script_path).exists()

    def test_wrapper_exports_all_model_env_vars(self, slurm_deployment):
        """Every key in model_info.env_vars must appear as `export KEY="value"` in the wrapper."""
        slurm_deployment.prepare()
        script_text = Path(slurm_deployment.script_path).read_text()

        for key, value in PR186_MODEL_ENTRY["env_vars"].items():
            # xP/yD are overridden by distributed.sglang_disagg.{prefill,decode}_nodes
            # in _prepare_slurm_multi_script; values match in this fixture so the
            # assertion still holds, but the source of truth is the model card.
            expected = f'export {key}="{value}"'
            assert expected in script_text, f"missing export for {key!r}: expected {expected!r}"

    def test_wrapper_is_slurm_multi_flag_set(self, slurm_deployment):
        """`_is_slurm_multi` flag must be set so deploy() can choose the bash branch."""
        slurm_deployment.prepare()
        assert getattr(slurm_deployment, "_is_slurm_multi", False) is True

    def test_wrapper_sbatch_header_uses_slurm_config(self, slurm_deployment):
        """SBATCH header reflects the model card's slurm block (partition, nodes, gpus, time, exclusive)."""
        slurm_deployment.prepare()
        script_text = Path(slurm_deployment.script_path).read_text()
        assert "#SBATCH --partition=amd-rccl" in script_text
        assert "#SBATCH --nodes=3" in script_text
        assert "#SBATCH --gpus-per-node=8" in script_text
        assert "#SBATCH --time=01:00:00" in script_text
        assert "#SBATCH --exclusive" in script_text
        # reservation is "" in the fixture, so no --reservation line
        assert "#SBATCH --reservation=" not in script_text

    def test_wrapper_disables_set_e_around_model_script(self, slurm_deployment):
        """Regression for Copilot C6: `set -e` at the top of the wrapper would
        terminate the script before SCRIPT_EXIT_CODE is captured and the
        completion marker is written if the model script exits non-zero.
        The wrapper must `set +e` immediately before the bash invocation,
        capture $?, then re-enable `set -e` before continuing."""
        slurm_deployment.prepare()
        script_text = Path(slurm_deployment.script_path).read_text()

        # Find the index of the bash <model_script> line and assert the
        # surrounding set +e / SCRIPT_EXIT_CODE / set -e ordering.
        lines = script_text.splitlines()
        bash_line_idx = next(
            (i for i, line in enumerate(lines) if line.strip().startswith("bash run_xPyD_models.slurm")),
            None,
        )
        assert bash_line_idx is not None, "wrapper must invoke the model script via bash"

        # The two preceding non-empty/non-comment lines must include `set +e`
        prelude = [l.strip() for l in lines[max(0, bash_line_idx - 5):bash_line_idx] if l.strip() and not l.strip().startswith("#")]
        assert "set +e" in prelude, (
            f"`set +e` must appear immediately before bash invocation, got prelude {prelude!r}"
        )

        # SCRIPT_EXIT_CODE must be captured immediately after, and -e re-enabled before exit
        suffix = [l.strip() for l in lines[bash_line_idx + 1:bash_line_idx + 5] if l.strip() and not l.strip().startswith("#")]
        assert suffix[0] == "SCRIPT_EXIT_CODE=$?", f"first non-comment line after bash must capture $?, got {suffix!r}"
        assert "set -e" in suffix, f"`set -e` must be re-enabled after capture, got {suffix!r}"

        # And the final exit must use the captured exit code
        assert "exit $SCRIPT_EXIT_CODE" in script_text


