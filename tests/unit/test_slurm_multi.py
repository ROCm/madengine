#!/usr/bin/env python3
"""
Unit tests for the slurm_multi launcher.

Locks in contract points so future refactors fail loudly if they regress:
1. `slurm_multi` is registered in `VALID_LAUNCHERS`.
2. `slurm-multi` (hyphen alias) normalizes to `slurm_multi`.
3. The wrapper SBATCH script generated by `_prepare_slurm_multi_script`
   exports every `model_info.env_vars` declared by the model card
   (mirrors the `pyt_sglang_disagg_qwen3-32b_short` entry from MAD-private PR #186).
4. The wrapper bash invocation is wrapped in `set +e` / `set -e` so a non-zero
   exit from the model script does not skip the completion-marker write.
5. `_execute_with_prebuilt_image` produces a manifest where `built_images.keys()
   == built_models.keys()` so `ContainerRunner.run_models_from_manifest()` joins
   them correctly (also exercises multi-model + nested-dict distributed configs
   to guard against the `tuple(sorted(items()))` unhashable regression).

Copyright (c) Advanced Micro Devices, Inc. All rights reserved.
"""

